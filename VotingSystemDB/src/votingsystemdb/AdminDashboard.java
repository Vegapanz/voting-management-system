    private void loadCandidateData() {
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/vms", "root", "");
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery("SELECT candidateID, name, votes FROM candidate")) {

            DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "Name", "Votes"}, 0);
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("candidateID"),
                    rs.getString("name"),
                    rs.getInt("votes")
                });
            }
            candidateTable.setModel(model);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading candidates: " + e.getMessage());
        }
    }

    private void loadVoterData() {
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/vms", "root", "");
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery("SELECT votersID, name, email, has_voted FROM voters")) {

            DefaultTableModel model = new DefaultTableModel(new String[]{"ID", "Name", "Email", "Has Voted"}, 0);
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("votersID"),
                    rs.getString("name"),
                    rs.getString("email"),
                    rs.getBoolean("has_voted") ? "Yes" : "No"
                });
            }
            voterTable.setModel(model);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error loading voters: " + e.getMessage());
        }
    }
    

    
