    private void resetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetButtonActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this, "Reset all votes and voter statuses?", "Confirm Reset", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) return;

        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/vms", "root", "");
             Statement stmt = conn.createStatement()) {

            stmt.executeUpdate("UPDATE candidate SET votes = 0");
            stmt.executeUpdate("UPDATE voters SET has_voted = FALSE");
            stmt.executeUpdate("DELETE FROM ballot");

            JOptionPane.showMessageDialog(this, "Votes and voter statuses have been reset.");
            loadCandidateData();
            loadVoterData();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error resetting data: " + e.getMessage());
        }
    }//GEN-LAST:event_resetButtonActionPerformed

