    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
       String selectedCandidate = null;

        if (kikoBarzaga.isSelected()) selectedCandidate = "Kiko Barzaga";
        else if (tarahPacardo.isSelected()) selectedCandidate = "Tarah Pacardo";
        else if (carlBrynne.isSelected()) selectedCandidate = "Carl Brynne";
        else if (michaelCabs.isSelected()) selectedCandidate = "Michael Cabs";
        else if (zheyAnn.isSelected()) selectedCandidate = "Zhey Ann-chan";
        else if (jRadioButton7.isSelected()) selectedCandidate = "AJ Salcedo";
        else if (jRadioButton8.isSelected()) selectedCandidate = "Rameses Moises";
        else if (jRadioButton9.isSelected()) selectedCandidate = "Tarah Barzaga";

        if (selectedCandidate == null) {
            JOptionPane.showMessageDialog(this, "Please select a candidate before submitting your vote.");
            return;
        }

        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/vms", "root", "");

            // Update candidate vote count
            String updateVoteSQL = "UPDATE candidate SET votes = votes + 1 WHERE name = ?";
            PreparedStatement voteStmt = conn.prepareStatement(updateVoteSQL);
            voteStmt.setString(1, selectedCandidate);
            int voteUpdated = voteStmt.executeUpdate();

            if (voteUpdated > 0) {
                // Get candidate ID from selected name
                String getCandidateIDQuery = "SELECT candidateID FROM candidate WHERE name = ?";
                PreparedStatement getIDStmt = conn.prepareStatement(getCandidateIDQuery);
                getIDStmt.setString(1, selectedCandidate);
                ResultSet idResult = getIDStmt.executeQuery();

                int candidateID = -1;
                if (idResult.next()) {
                    candidateID = idResult.getInt("candidateID");
                }
                idResult.close();
                getIDStmt.close();

                if (candidateID != -1) {
                    // Insert into ballot table
                    String insertBallotSQL = "INSERT INTO ballot (votersID, candidateID) VALUES (?, ?)";
                    PreparedStatement ballotStmt = conn.prepareStatement(insertBallotSQL);
                    ballotStmt.setInt(1, currentVotersID);
                    ballotStmt.setInt(2, candidateID);
                    ballotStmt.executeUpdate();
                    ballotStmt.close();
                }

                // Update voter as has_voted
                String updateVoterSQL = "UPDATE voters SET has_voted = TRUE WHERE votersID = ?";
                PreparedStatement voterStmt = conn.prepareStatement(updateVoterSQL);
                voterStmt.setInt(1, currentVotersID);
                voterStmt.executeUpdate();
                voterStmt.close();

                // Show receipt
                LocalDateTime now = LocalDateTime.now();
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");

                String receipt = "\uD83D\uDDF3 Vote Receipt\n\n" +
                    "Voter ID: " + currentVotersID + "\n" +
                    "Candidate: " + selectedCandidate + "\n" +
                    "Time: " + now.format(formatter) + "\n\n" +
                    "Thank you for voting!";

                JOptionPane.showMessageDialog(this, receipt, "Vote Submitted", JOptionPane.INFORMATION_MESSAGE);
                disableAllRadioButtons();
                submitButton.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(this, "Failed to record vote. Candidate not found.");
            }

            voteStmt.close();
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error submitting vote: " + e.getMessage());
        }
    }//GEN-LAST:event_submitButtonActionPerformed

