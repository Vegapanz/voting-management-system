    private void checkIfAlreadyVoted() {
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/vms", "root", "")) {
            String sql = "SELECT has_voted FROM voters WHERE votersID = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, currentVotersID);
            ResultSet rs = ps.executeQuery();

            if (rs.next() && rs.getBoolean("has_voted")) {
                JOptionPane.showMessageDialog(this, "You have already voted.");
                submitButton.setEnabled(false);
                disableAllRadioButtons();
            }

            rs.close();
            ps.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error checking vote status: " + e.getMessage());
        }
    }

